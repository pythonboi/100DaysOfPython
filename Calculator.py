# Create a function for Adding

def add(n1, n2):
    return n1 + n2


# Create a function for Subtracting

def subtract(n1, n2):
    return n1 - n2


# Create a function for Multiplication

def multiply(n1, n2):
    return n1 * n2


# Create a function for Division

def divide(n1, n2):
    return n1 / n2
